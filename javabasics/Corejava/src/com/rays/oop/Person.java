package com.rays.oop;

import java.sql.Date;

public class Person {
     private String name;
     private String addres;
     private  Date dob;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAddres() {
		return addres;
	}
	public void setAddres(String addres) {
		this.addres = addres;
	}
	public Date getDob() {
		return dob;
	}
	public void setDob(Date dob) {
		this.dob = dob;

		
	}
     
     
     
}
